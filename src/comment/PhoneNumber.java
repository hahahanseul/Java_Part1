package comment;
class PhoneNumber {
	public static void main(String[] args) {
		// String型の変数を３つ宣言する
		String tel1;
		String tel2;
		String tel3;
		// ３つの変数に文字列を代入する
		tel1 = "03";
		tel2 = "1234";
		tel3 = "5678";
		System.out.print("電話番号：");
		// 変数の値をディスプレイに表示する
		System.out.println(tel1 + "-" + tel2 + "-" + tel3);
	}
}
