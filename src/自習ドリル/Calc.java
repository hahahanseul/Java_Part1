package 自習ドリル;

public class Calc {
	public static int add(int n1, int n2) {
		int ans = n1 + n2;
		return ans;
	}

	public static int sub(int n1, int n2) {
		int ans = n1 - n2;
		return ans;
	}

	public static int multi(int n1, int n2) {
		int ans = n1 * n2;
		return ans;
	}

	public static int div(int n1, int n2) {
		int ans = n1 / n2;
		return ans;
	}
}
